    private void llenarTabla() {
        String[][] lista = new Cotizacion().listarxStarSoft();
        DefaultTableModel modelo = (DefaultTableModel) tablaArchivo.getModel();
        modelo.setRowCount(0);
        for (String[] c : lista) {
            if (c[0] != null) {
                modelo.addRow(new Object[]{
                    c[0], c[1], c[3], c[2], c[4]
                });
            }

        }
        if (modelo.getRowCount() == 0) {
            modelo.addRow(new Object[]{
                "", "", "No hay Cots.", ""
            });
            limpiarCampos();
        }
    }

