    private void tablaArchivoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaArchivoMouseClicked
        String numCot = "";
        String monto = "";
        if (validarSeleccion()) {
            if (evt.getClickCount() == 1) {
                numCot = tablaArchivo.getValueAt(tablaArchivo.getSelectedRow(), 0).toString().substring(3, 7);
                monto = tablaArchivo.getValueAt(tablaArchivo.getSelectedRow(), 4).toString();
            } else if (evt.getClickCount() == 2) {
                numCot = tablaArchivo.getValueAt(tablaArchivo.getSelectedRow(), 0).toString().substring(3, 7);
                monto = tablaArchivo.getValueAt(tablaArchivo.getSelectedRow(), 4).toString();
                
                nc.numeroCot.setText(numCot);
                nc.montoTxt.setText(monto);
                nc.formaPago.requestFocus();
                this.dispose();
            }
        }
    }//GEN-LAST:event_tablaArchivoMouseClicked

