    private void llenarTabla() {

        ArrayList<Cotizacion> lista = (ArrayList) new Cotizacion().ListadoCotAvance().clone();
        vaciarTabla();
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        for (Cotizacion c : lista) {
            modelo.addRow(new Object[]{
                c.getNumeroCot(), c.getNumOt(), c.getPresupuesto().getCliente(), c.getEstado()
            });
        }
    }

    private void llenarTabla(String texto) {

        ArrayList<Cotizacion> lista = (ArrayList) new Cotizacion().ListadoCotAvance(texto).clone();
        vaciarTabla();
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        for (Cotizacion c : lista) {
            modelo.addRow(new Object[]{
                c.getNumeroCot(), c.getNumOt(), c.getPresupuesto().getCliente(), c.getEstado()
            });
        }
    }

    private void vaciarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        modelo.setRowCount(0);
    }

