    public void verDatos() {
        
        ArrayList<Cotizacion> vals;
        String vendedor = (Main.getVendedor()).equals("vacio") ? "vacio" : Main.getVendedor();

        String cotMes;
        String cotMesMias;
        String ventasMes;

        String cotHoy;
        String cotHoyMias;
        String ventasHoy;

        String mes = new SimpleDateFormat("MM").format(new Date());
        vals = new ArrayList();
        vals = (ArrayList) new Cotizacion().listarCotxMes(mes).clone();
        int a = 0;
        for (Cotizacion c : vals) {
            a++;
        }
        cotMes = a + "";
        info1.setText(cotMes);

        a = 0;
        vals = new ArrayList();
        vals = (ArrayList) new Cotizacion().listarxMesVendedor(mes, vendedor).clone();
        for (Cotizacion c : vals) {
            a++;
        }
        cotMesMias = a + "";
        info2.setText(cotMesMias);

        ventasMes = new Cotizacion().getVentasMes() + "";
        info3.setText(ventasMes);

        String fecha = new SimpleDateFormat("dd/MM/yyyy").format(new Date());
        vals = (ArrayList) new Cotizacion().listarFecha(fecha).clone();
        a = 0;
        for (Cotizacion c : vals) {
            a++;
        }
        cotHoy = a + "";
        info4.setText(cotHoy);

        vals = new ArrayList();

        vals = (ArrayList) new Cotizacion().listarFechaVendedor(fecha, vendedor).clone();
        a = 0;
        for (Cotizacion c : vals) {
            a++;
        }
        cotHoyMias = a + "";
        info5.setText(cotHoyMias);

        ventasHoy = new Cotizacion().getVentasFecha(fecha) + "";
        info6.setText(ventasHoy);
        //autoVerDatos();
    }

    private void autoVerDatos() {

        TimerTask timerTask = new TimerTask() {
            public void run() {
                verDatos();
            }
        };
        Timer timer = new Timer();
        timer.scheduleAtFixedRate(timerTask, 60000, 60000);
    }
