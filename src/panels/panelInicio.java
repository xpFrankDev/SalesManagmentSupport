    public void setOpcion() {

        switch (opcion) {
            case 1:
                opcion1.setSelected(true);
                opcion2.setSelected(false);
                opcion3.setSelected(false);
                Main.scrollContenido.setViewportView(inf);                
                
                break;
            case 2:
                opcion2.setSelected(true);
                opcion1.setSelected(false);
                opcion3.setSelected(false);
                 Main.scrollContenido.setViewportView(estVentas);  
                 estVentas.actualizarGrafico1();
                 estVentas.setVentas();
            break;
            case 3:
                opcion2.setSelected(false);
                opcion1.setSelected(false);
                opcion3.setSelected(true);
                Main.scrollContenido.setViewportView(estToneladas);  
                estToneladas.setDatos();
                //setDatos
            break;
                
        }
    }

